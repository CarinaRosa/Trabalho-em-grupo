package Projeto2_2b.Agenda.Teste;

public class Endereco extends Contato{
    private String logradouro;
    private int numero;
    private String complemento;
    private String bairro;
    private String cep;
    private String cidade;

    public Endereco(){

    }
    public Endereco(String nome, String logradouro, int numero,String complemento, String bairro, String cep, String cidade){
        super(nome);
        this.logradouro = logradouro;
        this.numero = numero;
        this.complemento = complemento;
        this.bairro = bairro;
        this.cep = cep;
        this.cidade = cidade;
    }

    @Override
    public String getDados(){
        return super.getDados() + ":\n" + String.format("Rua: %s\n Numero: %f\n Complemento: %s\n Bairro: %s\n CEP: %s\n Cidade: %s", this.logradouro, this.numero, this.complemento, this.bairro, this.cep, this.cidade);
    }
}
